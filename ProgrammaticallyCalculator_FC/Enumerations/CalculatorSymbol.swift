enum CalculatorSymbol: String {
    
    // OPERATIONS
    case add = "+"
    case subtract = "-"
    case multiply = "×"
    case divide = "÷"
    
    // CONTROLS
    case equal = "="
    case ac = "AC"
    case percent = "%"
    case changeSign = "+/-"
    
    // NUMBERS AND DOT
    case dot = "."
    case n0 = "0"
    case n1 = "1"
    case n2 = "2"
    case n3 = "3"
    case n4 = "4"
    case n5 = "5"
    case n6 = "6"
    case n7 = "7"
    case n8 = "8"
    case n9 = "9"

}
